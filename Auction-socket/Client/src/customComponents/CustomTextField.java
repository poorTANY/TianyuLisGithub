package customComponents;

import java.awt.Color;
import java.awt.Font;

import javax.swing.BorderFactory;
import javax.swing.JTextField;

/** @author jci574
 * 
 *  CustomTextField extends the class JTextField with all the same functionalities as 
 *  JTextField but with a modified border, background colour, foreground colour and font. 
 *
 */
public class CustomTextField extends JTextField{
	
	private static final long serialVersionUID = 1L;
	
	/** CustomTextField(String b, int a) is a constructor for the CustomTextField class 
	 * where the text is set to the String b and the size is set to the int a.
	 * 
	 * @param b the text to be added to the label as String.
	 * @param a the size to set the label to.
	 */
	public CustomTextField(String b, int a){
		super(b, a);
		setBorder(BorderFactory.createLineBorder(Color.black,1));
		setBackground(Color.WHITE);
		setFont(new Font("Apple Casual", 1, 12));
		setForeground(Color.BLACK);
	}
	
	/** CustomTextField() is a constructor for the CustomTextField class where the
	 * test is set to null. 
	 */
	public CustomTextField(){
		
		setBorder(BorderFactory.createLineBorder(Color.black,1));
		setBackground(Color.WHITE);
		setFont(new Font("Apple Casual", 1, 12));
		setForeground(Color.BLACK);
	}
	
}
